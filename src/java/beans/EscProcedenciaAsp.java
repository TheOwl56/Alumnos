public class EscProcedenciaAsp {
    private String  Escuela;
    private String ClaveEsc;
    private String TipoEsc;
    private String MesInicio;
    private String AnioInicio;
    private String MesFin;
    private String AnioFin;
    private String promedio;

    /**
     * @return the Escuela
     */
    public String getEscuela() {
        return Escuela;
    }

    /**
     * @param Escuela the Escuela to set
     */
    public void setEscuela(String Escuela) {
        this.Escuela = Escuela;
    }

    /**
     * @return the ClaveEsc
     */
    public String getClaveEsc() {
        return ClaveEsc;
    }

    /**
     * @param ClaveEsc the ClaveEsc to set
     */
    public void setClaveEsc(String ClaveEsc) {
        this.ClaveEsc = ClaveEsc;
    }

    /**
     * @return the TipoEsc
     */
    public String getTipoEsc() {
        return TipoEsc;
    }

    /**
     * @param TipoEsc the TipoEsc to set
     */
    public void setTipoEsc(String TipoEsc) {
        this.TipoEsc = TipoEsc;
    }

    /**
     * @return the MesInicio
     */
    public String getMesInicio() {
        return MesInicio;
    }

    /**
     * @param MesInicio the MesInicio to set
     */
    public void setMesInicio(String MesInicio) {
        this.MesInicio = MesInicio;
    }

    /**
     * @return the AnioInicio
     */
    public String getAnioInicio() {
        return AnioInicio;
    }

    /**
     * @param AnioInicio the AnioInicio to set
     */
    public void setAnioInicio(String AnioInicio) {
        this.AnioInicio = AnioInicio;
    }

    /**
     * @return the MesFin
     */
    public String getMesFin() {
        return MesFin;
    }

    /**
     * @param MesFin the MesFin to set
     */
    public void setMesFin(String MesFin) {
        this.MesFin = MesFin;
    }

    /**
     * @return the AnioFin
     */
    public String getAnioFin() {
        return AnioFin;
    }

    /**
     * @param AnioFin the AnioFin to set
     */
    public void setAnioFin(String AnioFin) {
        this.AnioFin = AnioFin;
    }

    /**
     * @return the promedio
     */
    public String getPromedio() {
        return promedio;
    }

    /**
     * @param promedio the promedio to set
     */
    public void setPromedio(String promedio) {
        this.promedio = promedio;
    }
    
}
